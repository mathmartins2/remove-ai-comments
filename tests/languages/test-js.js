// Arquivo de teste JavaScript
// Este é um exemplo de arquivo JavaScript com comentários

function hello() {
  // Comentário dentro da função
  console.log('Hello, world!');
  
  // Outro comentário indentado
  return true;
}

// Classe de exemplo
class ExampleClass {
  constructor() {
    // Comentário no construtor
    this.value = 42;
  }
  
  // Método com comentário
  getValue() {
    // Retorna o valor
    return this.value;
  }
}

// Exportando a classe
module.exports = ExampleClass; 